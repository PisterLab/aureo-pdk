($input_file, $output_file) = @ARGV;

open(infile, $input_file);
open(outfile, ">$output_file");
while (<infile>) {

  $line = lc($_);
  if (index($line, "nan") == -1) {
    printf outfile $_;
  }

}
close(infile);
close(outfile);
